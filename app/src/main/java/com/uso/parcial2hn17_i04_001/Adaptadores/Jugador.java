package com.uso.parcial2hn17_i04_001.Adaptadores;

public class Jugador {
    private String Jugadorname;
    private String Jugadorpuntos;
    private String JugadorIntentos;

    public String getJugadorname() {
        return Jugadorname;
    }

    public void setJugadorname(String jugadorname) {
        Jugadorname = jugadorname;
    }

    public String getJugadorpuntos() {
        return Jugadorpuntos;
    }

    public void setJugadorpuntos(String jugadorpuntos) {
        Jugadorpuntos = jugadorpuntos;
    }

    public String getJugadorIntentos() {
        return JugadorIntentos;
    }

    public void setJugadorIntentos(String jugadorIntentos) {
        JugadorIntentos = jugadorIntentos;
    }
}
